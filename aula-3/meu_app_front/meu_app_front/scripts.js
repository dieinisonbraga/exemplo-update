// função que verifica se um produto já está na lista/banco
const verificaProdutoAdicionado = (inputProduct) => {
  let nomeProdutos = document.getElementsByClassName("nomeProduto");
  // percorre lista de produtos para saber se vou adicionar um novo, ou atualizar
  for(produto of nomeProdutos) {
    // se o produto já está na tabela
    if (produto.textContent == inputProduct) return true;
  }
  // caso contrário
  return false;
}

/*
  --------------------------------------------------------------------------------------
  Função para adicionar um novo item com nome, quantidade e valor 
  --------------------------------------------------------------------------------------
*/
const newItem = () => {
  let inputProduct = document.getElementById("newInput").value;
  let inputQuantity = document.getElementById("newQuantity").value;
  let inputPrice = document.getElementById("newPrice").value;
  if (inputProduct === '') {
    alert("Escreva o nome de um item!");
  } else if (isNaN(inputQuantity) || isNaN(inputPrice)) {
    alert("Quantidade e valor precisam ser números!");
  } else if(verificaProdutoAdicionado(inputProduct)) {
    console.log(verificaProdutoAdicionado(inputProduct));
    updateItem(inputProduct, inputQuantity, inputPrice);
    alert("Item atualizado!");
    location.reload(); // atualizando a interface com a lista atualizada
  } else {
    postItem(inputProduct, inputQuantity, inputPrice)
    insertList(inputProduct, inputQuantity, inputPrice)
    alert("Item adicionado!")
  }
}

/*
  --------------------------------------------------------------------------------------
  Função para inserir items na lista apresentada
  --------------------------------------------------------------------------------------
*/
const insertList = (nameProduct, quantity, price) => {
  var item = [nameProduct, quantity, price]
  var table = document.getElementById('myTable');
  var row = table.insertRow();

  for (var i = 0; i < item.length; i++) {
    var cel = row.insertCell(i);
    if ( i == 0 ) cel.setAttribute("class", "nomeProduto"); // adiciona essa classe apenas na primeira propriedade
    cel.textContent = item[i];
  }
  insertButton(row.insertCell(-1))
  document.getElementById("newInput").value = "";
  document.getElementById("newQuantity").value = "";
  document.getElementById("newPrice").value = "";

  removeElement()
}